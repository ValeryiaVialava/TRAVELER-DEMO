// eslint-disable-next-line no-unused-vars
export function getCountriesForGame(region, difficulty) {
  // The region selector defines scope; "World" means every country in the
  // dataset. Tier is left on each country for future use (e.g. question
  // weighting by familiarity) but it no longer filters the pool — the user
  // expects "World" to highlight the whole world, not a 89-country subset.
  if (region === 'all') return COUNTRY_DATA
  return COUNTRY_DATA.filter(c => c.region === region)
}
