export function getCountriesForGame(region, difficulty) {
  const maxTier = difficulty === 'easy' ? 1 : difficulty === 'normal' ? 2 : 3
  return COUNTRY_DATA.filter(c => {
    const regionMatch = region === 'all' || c.region === region
    return regionMatch && c.tier <= maxTier
  })
}
